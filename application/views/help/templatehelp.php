<p><span style="text-decoration: underline;"><strong>Help for creating a template</strong></span></p>
<p>&nbsp;</p>
<p>There is one easy page for creating a template and this is how you do it:</p>
<ol>
<li><em>Template Title:</em> This is the title of the template, this is how you will access and use the template.</li>
<li><em>Template Description:</em> This should be how you explain what the template should be used for and can be as long as you need.</li>
<li><em>Visibility:&nbsp;</em>Decides if only you or everyone can use this template.</li>
<li><em>File:</em> This is the file that creates the template, needs to be .kml or .kmz.</li>
<li><em>Admin Level:</em> What level of admin is required to use the template.</li>
<li><em>How many decimal places to round to:</em> How accurate the borders on the regions are, the lower the rounding, the faster the template loads.</li>
<li><em>How far back to remember rounding:</em>&nbsp;</li>
<li><em>By default, what should the center point latitude be:</em> This is the latitude for the template to focus on and first appear at.</li>
<li><em>By default, what should the center point longitude be:</em> This is the longitude for the template to focus on and first appear at.</li>
<li><em>By default what should this map zoom to:</em> This is how closs the template should zoom automatically, the small the number, the farther zoomed out the template is.</li>
</ol>
